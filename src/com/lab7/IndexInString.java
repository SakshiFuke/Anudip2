public class IndexInString {

	public static void main(String[] args) {
		String str="Sakshi Fuke";
		System.out.println(("String is : "+str));
		
		int index1=str.charAt(0);
		int index2=str.charAt(5);
		
		System.out.println("the String at 0th position is: "+(char)index1);
		System.out.println("the String at 5th position is: "+(char)index2);
	}

}
